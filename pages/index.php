//функция закрытия попап окна
function closePopup() {
        $(".popup").fadeOut(); 
};

$(".open_popup_btn").click(function(){
    let pop_up = $(this).attr("data-popup");
    $("#" + pop_up).fadeIn();
    $('#overlay').fadeIn();
});

$(".close_popup_btn").click(function(){
    closePopup();
    $('#overlay').fadeOut();
});

$(".dop_info_btn").click(function(){
    if ($(".dop_info_menu_plain").is(':visible')) {
        $(".dop_info_menu_plain").fadeOut();
        $(this).removeClass("dop_info_btn_open")
        $(".dop_info_btn_text").css("color", "white");
        $(".dop_info_btn_img").attr("src", "img/Down Button.svg");
    }
    else{ 
        $(".dop_info_menu_plain").fadeIn();
        $(this).addClass("dop_info_btn_open");
        $(".dop_info_btn_text").css("color", "#444444");
        $(".dop_info_btn_img").attr("src", "img/Down Button (1).svg");
    }
});
</script>
